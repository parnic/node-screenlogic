'use strict';

const assert = require('assert');
const HLEncoder = require('../PasswordEncoder').HLEncoder;

describe('Password encoder', function() {
  it('encodes correctly', function() {
    let pw = new HLEncoder('1234').getEncryptedPassword('00-00-00-00-00-005');
    assert.equal(true, pw.equals(Buffer.from([0x4e, 0x71, 0x7f, 0x2e, 0xff, 0xda, 0xfd, 0x5d, 0xdf, 0x34, 0x26, 0x32, 0x23, 0xd7, 0x11, 0x06, 0x72, 0xbc, 0x40, 0x0f, 0x61, 0x81, 0x9f, 0xc8, 0x84, 0xcc, 0x6b, 0xaa, 0x0b, 0x16, 0x77, 0xab])));
  });
});
